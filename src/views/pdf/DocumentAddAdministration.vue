            /**
              * Application file mime type
              */
            "application/pdf"
            ];
            
            // allowed max size in MB
            let allowed_size_mb = 25;

            // Validate file type
            if(allowed_mime_types.indexOf(file.type) == -1) {
                notify.error({
                    title: "бһҜбһҖбһҹбһ¶бһҡбһҷбҹ„бһ„" ,
                    description: "бһҜбһҖбһҹбһ¶бһҡбһ“бҹҒбҹҮбһҮбһ¶бһ”бҹ’бһҡбһ—бҹҒбһ‘бҹ– "+ file.type +"бҹ” бһўбһ“бһ»бһүбҹ’бһүбһ¶бһҸбһҸбҹӮбһҜбһҖбһҹбһ¶бһҡбһҠбҹӮбһӣбһҳбһ¶бһ“бһ”бҹ’бһҡбһ—бҹҒбһ‘бһҮбһ¶ PDFбҹ”" ,
                    duration: 3000
                })
                return;
            }

            // Validate file size
            if(file.size > allowed_size_mb*1024*1024) {
                notify.error({
                    title: "бһҜбһҖбһҹбһ¶бһҡбһҷбҹ„бһ„" ,
                    description: "бһ‘бҹҶбһ бҹҶбһ“бҹғбһҜбһҖбһҹбһ¶бһҡбһӮбһәбҹ– " + (file.size/1024/1024).toFixed(2) + " бһҳбҹҒбһҖбһ¶бһ”бҹғ (MB) бһӣбһҫбһӣбһ‘бҹҶбһ бҹҶбһҠбҹӮбһӣбһҖбҹҶбһҺбһҸбҹӢбһӮбһә бҹҘ бһҳбҹҒбһҖбһ¶бһ”бҹғбҹ”" ,
                    duration: 3000
                })
            return;
            }


            let reader = new FileReader();
            reader.onerror = function(e) {
                console.log('On error');
            };
            reader.onprogress = function(e) {
                console.log('On progress');
            };
            reader.onabort = function(e) {
                console.log('On abort');
            };
            reader.onloadstart = function(e) {
                console.log( "On load start" )
            };
